import java.util.*;

public class Math {
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
         int a = sc.nextInt();
         int b = sc.nextInt();

         // Sum of two numbers taking as the user input
         int sum = a+b; // Here we can do multiple operation like -, *, /, %
         System.out.println(sum); // In place of the sum we can do difference, product, division etc. 
         sc.close();

    }
}
